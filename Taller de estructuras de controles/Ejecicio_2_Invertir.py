#Suponga que un individuo decide invertir su capital en un banco y desea saber cuánto dinero ganará después de un mes si el banco paga a razón de 2% mensual
Capital= int(input("Ingrese el capital a invertir:  "))
Mes1= 1
Porcentaje= 0.02 
Inversion= (Capital*Porcentaje)
Res= (Inversion*Mes1)
print (f"Su ganacias en el mes {Mes1} es ==>  {Res} $ ")